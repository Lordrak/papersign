    {
        request()->validate([
            'nom'=>'required',
            'prenom'=>'required',
            'formation'=>'required',
            'lieu'=>'required',
            'email_user'=>'required',
            'email_teacher'=>'required',
            'email_mdef'=>'required'
        ]);

        Apprenant::create($request->all());
